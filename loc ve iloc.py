# gözlem ve değişken secimi: loc & iloc

import numpy as np
import pandas as pd
m= np.random.randint(1,30,size=(10,3))
df= pd.DataFrame(m,columns=["var1","var2","var3"])
df

#loc : tanımlandığı şekli ile seçim yapmak için kullanılır
df.loc[0:3] # 3 te dahil 

# =============================================================================
# var1  var2  var3
# 0    29    12    24
# 1    21     6    21
# 2     9     5    20
# 3    11    20    11
# =============================================================================


#iloc: alışık olduğumuz indeksleme mantığı ile seçim yapar.
df.iloc[0:3]   # 3 e kadar

# var1  var2  var3
# =============================================================================
# 0    29    12    24
# 1    21     6    21
# 2     9     5    20
# =============================================================================

df.iloc[:3,:2]

# =============================================================================
# var1  var2
# 0    29    12
# 1    21     6
# 2     9     5
# =============================================================================

df.loc[0:3,"var3"]

# =============================================================================
# 0    24
# 1    21
# 2    20
# 3    11
# Name: var3, dtype: int32
# 
# 
# =============================================================================




